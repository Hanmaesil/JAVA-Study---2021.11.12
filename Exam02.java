
public class Exam02 {

	public static void main(String[] args) {
		
		//실습 : FOR문을 사용하여 1에서 100까지 다음과 같은 순서로 출력
		
		for(int i = 1; i <=100; i++) {
			if(i%2 == 0) {
				System.out.print(i * -1 + "\t");// or -i or "-" + i
			}else
				System.out.print(i + "\t");
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
